    private void jButtonAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAltaActionPerformed
        if (logicaCarreras.getListaCarreras().get(seleccion).getListaCorredores().size() < logicaCarreras.getListaCarreras().get(seleccion).getMaxparticipantes()) {
            Listaparticipantesacarrera lcc = new Listaparticipantesacarrera(this, true, seleccion, logicacorredores, logicaCarreras);
            lcc.setVisible(true);
            lcc.setLocationRelativeTo(null);
            rellenarTablaCorredores();
        } else {
            JOptionPane.showMessageDialog(this, "La carrera ya esta llena");
        }
    }//GEN-LAST:event_jButtonAltaActionPerformed

