    private void rellenarTablaCorredores() {
        if (finalizada) {
            Carreras carrera = logicaCarreras.getListaCarrerasFinalizadas().get(seleccion);
            jTableCorredores.setModel(new TablaParticipantes(carrera.getListaCorredores()));
        } else {
            Carreras carrera = logicaCarreras.getListaCarreras().get(seleccion);
            jTableCorredores.setModel(new TablaParticipantes(carrera.getListaCorredores()));
        }
    }

